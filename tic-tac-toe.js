var namespace = "http://www.w3.org/2000/svg"
var turn = "player1"
function doSomething() {
  var newShape= document.createElementNS(namespace, "circle")
newShape.setAttribute("cx", 50)
newShape.setAttribute("cy", 40)
newShape.setAttribute("r", 15)
if (turn == "player1"){
  newShape.setAttribute("fill", "pink")
  turn = "player2"
}else{
  newShape.setAttribute("fill", "blue")
  turn = "player1"
}
 var canvas = document.getElementById("game-board")
canvas.appendChild(newShape)
}


